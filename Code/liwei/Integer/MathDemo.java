package liwei.Integer;

public class MathDemo {
	
	 public static void main(String[] args) {
		// 四舍五入
		 double d = Math.round(5.41);
		 System.out.println("四舍五入5.41 = "+d);
		 // 上取整
		 double c = Math.ceil(5.1);
		 System.out.println("上取整 "+c);
		 // 下取整
		 double f = Math.floor(5.1);
		 System.out.println("下取整 "+f);
	}
}
